"""
บทที่ 2: ตัวแปร (Variables) และ หน่วยความจำ (Memory)
===================================================

ในภาษาอื่น เช่น C หรือ Java คุณต้อง "ประกาศประเภท" ก่อนใช้งาน
  int x = 10;        // Java: ประกาศว่า x เป็น integer
  String name = "A";  // Java: ประกาศว่า name เป็น string

ใน Python ไม่ต้องทำแบบนี้! Python จะ "เดา" ประเภทให้เอง
เรียกว่า Dynamic Typing (การกำหนดประเภทแบบไดนามิก)

สิ่งสำคัญที่ต้องเข้าใจ:
  - ตัวแปรใน Python คือ "ป้ายชื่อ" ที่ชี้ไปยังข้อมูลในหน่วยความจำ
  - ไม่ใช่ "กล่อง" ที่เก็บข้อมูลไว้ตรง ๆ เหมือนภาษาอื่น
"""


# ============================================================
# 2.1 การสร้างตัวแปร (Assignment)
# ============================================================
# ใช้เครื่องหมาย = ในการ "กำหนดค่า" ให้ตัวแปร
# = ใน Python ไม่ใช่ "เท่ากับ" แต่คือ "ให้ค่า" (assign)

name = "สมชาย"
age = 25
height = 175.5
is_student = True

print("--- 2.1 การสร้างตัวแปร ---")
print(f"name    = {name}    (ประเภท: {type(name).__name__})")
print(f"age     = {age}     (ประเภท: {type(age).__name__})")
print(f"height  = {height}  (ประเภท: {type(height).__name__})")
print(f"is_student = {is_student}  (ประเภท: {type(is_student).__name__})")
print()


# ============================================================
# 2.2 ตัวแปรใน Python คือ "ตัวอ้างอิง" (Reference)
# ============================================================
# นี่คือหัวใจสำคัญที่มือใหม่มักไม่เข้าใจ!
# ตัวแปรไม่ได้เก็บ "ค่า" ไว้โดยตรง แต่เก็บ "ที่อยู่" (address) ของค่า

print("--- 2.2 ตัวแปรคือตัวอ้างอิง ---")

a = 10
b = a       # b ไม่ได้คัดลอกค่า 10 ไปเก็บเอง
            # b ชี้ไปยัง "ที่เดียวกับ" a

print(f"a = {a}, id(a) = {id(a)}")
print(f"b = {b}, id(b) = {id(b)}")
print(f"a กั b ชี้ที่เดียวกัน? {a is b}")  # True! (id เหมือนกัน)
print()

# เมื่อเปลี่ยนค่า a เป็น 20
a = 20      # a จะชี้ไปยัง "ค่าใหม่" 20 (สร้าง object ใหมนใน memory)
print(f"หลัง a = 20:")
print(f"a = {a}, id(a) = {id(a)}")  # id เปลี่ยน!
print(f"b = {b}, id(b) = {id(b)}")  # b ยังเป็น 10 เหมือนเดิม
print(f"a กั b ชี้ที่เดียวกัน? {a is b}")  # False
print()


# ============================================================
# 2.3 การตั้งชื่อตัวแปร (Naming Rules)
# ============================================================
# กฎที่ต้องทำตาม (ผิดกฎ = Error):
#   1. ต้องขึ้นต้นด้วยตัวอักษร (a-z, A-Z) หรือ underscore (_)
#   2. หลังตัวแรกเป็นต้นไปใช้ได้: ตัวอักษร, ตัวเลข (0-9), underscore
#   3. ความไวต่อตัวพิมพ์ใหญ่เล็ก (Case-sensitive)
#   4. ห้ามใช้ "คำสงวน" (keywords) เช่น if, for, while, class, ...

print("--- 2.3 การตั้งชื่อตัวแปร ---")

# ✅ ชื่อที่ถูกต้อง
my_name = "สมหญิง"
_private = "ตัวแปรส่วนตัว (คอนเวนชั่น)"
my_name2 = "มีตัวเลขต่อท้ายได้"
my_full_name = "Snake Case: คั่นด้วย underscore"
MyName = "Camel Case: ตัวแรกของแต่ละคำตัวใหญ่"
MAX_SIZE = 100  # ตัวพิมพ์ใหญ่ทั้งหมด = ค่าคงที่ (คอนเวนชั่น)

print(f"my_name = {my_name}")
print(f"my_name2 = {my_name2}")
print(f"my_full_name = {my_full_name}")
print(f"MAX_SIZE = {MAX_SIZE}")

# ความไวต่อตัวพิมพ์
Cat = "แมว"
cat = "แมวตัวเล็ก"
print(f"Cat = {Cat}, cat = {cat}")  # คนละตัวแปรกัน!
print()

# ❌ ชื่อที่ไม่ถูกต้อง (ถ้า uncomment จะ Error)
# 2fast = "ตัวเลขนำหน้าไม่ได้"      # SyntaxError
# my-name = "มีขีดกลางไม่ได้"      # SyntaxError
# my name = "มีช่องว่างไม่ได้"       # SyntaxError
# for = "คำสงวน ใช้เป็นชื่อไม่ได้"  # SyntaxError


# ============================================================
# 2.4 การให้ค่าหลายตัวแปร (Multiple Assignment)
# ============================================================
print("--- 2.4 การให้ค่าหลายตัวแปร ---")

# ให้ค่าเดียวกันหลายตัวแปร
x = y = z = 0
print(f"x={x}, y={y}, z={z}")

# ให้ค่าต่างกันหลายตัวแปรในบรรทัดเดียว (Tuple Unpacking)
name, age, city = "สมศรี", 30, "กรุงเทพ"
print(f"name={name}, age={age}, city={city}")
print()


# ============================================================
# 2.5 การสลับค่า (Swap)
# ============================================================
print("--- 2.5 การสลับค่า ---")
a, b = 10, 20
print(f"ก่อนสลับ: a={a}, b={b}")

# วิธีปกติ (ภาษาอื่น): ใช้ตัวแปรชั่วคราว
# temp = a; a = b; b = temp

# วิธี Python: ใช้ Tuple Unpacking (สวยและสั้น!)
a, b = b, a
print(f"หลังสลับ: a={a}, b={b}")
print()


# ============================================================
# 2.6 ค่าคงที่ (Constants) ใน Python
# ============================================================
# Python ไม่มีกลไกบังคับ "ค่าคงที่" แต่มีคอนเวนชั่น:
#   ตัวพิมพ์ใหญ่ทั้งหมด = ค่าคงที่ (ไม่ควรเปลี่ยน)
#   แต่จริง ๆ แล้วเปลี่ยนได้ (Python ไม่ป้องกัน)

print("--- 2.6 ค่าคงที่ ---")
PI = 3.14159265
MAX_USERS = 1000
APP_NAME = "My App"
print(f"PI = {PI}")
print(f"MAX_USERS = {MAX_USERS}")
print(f"APP_NAME = {APP_NAME}")
print()

